// positive ot Negative numver 
var a =  prompt("Enter a number: ");
if (a > 0) {
    alert("Positive number");
    console.log(`you have enter a ${a} Positive number`);
    document.write(`you have enter a ${a} Positive number`);
}
else if (a < 0) {
    alert("Negative number");
    console.log(`you have enter a ${a} Negative number`);
    document.write(`you have enter a ${a} Negative number`);
}
else {
    alert("Zero");
    console.log(`you have enter a ${a} Zero`);
    document.write(`you have enter a ${a} Zero`);
}