// even or odd number
var a =  prompt("Enter a number: ");
if (a % 2 == 0) {
    alert("Even number");
    console.log(`you have enter a ${a} which is Even number`);
    document.write(`you have enter a ${a} which is Even number`);
}
else {
    alert("Odd number");
    console.log(`you have enter a ${a} which is  Odd number`);
    document.write(`you have enter a ${a} which is Odd number`);
}
